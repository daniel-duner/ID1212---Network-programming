package common;

public class Print {
    public static void print(String msg){
        System.out.println(msg);
    }
    public static void print(int msg){
        System.out.println(msg);
    }
    public static void print(char msg){
        System.out.println(msg);
    }
    public static void print(long msg){
        System.out.println(msg);
    }
    public static void print(boolean msg){
        System.out.println(msg);
    }
}
